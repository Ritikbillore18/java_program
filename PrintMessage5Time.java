//Write a program to print Hello and then print your name five times

class PrintMessage5Time {
  public static void main(String[] args) {
    for (int i = 0; i < 5; i++) {
      System.out.println("Hello");
      for (int j = 0; j < 5; j++) {
        System.out.println("Sterling Institute");
      }
    }
  }
}
