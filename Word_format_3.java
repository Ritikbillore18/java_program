// program to check character is vowel or constant
class Word_format_3
{
public static void main(String args[])
{
 char ch='o';
 switch(ch)
 {
  case 'a':
  case 'i':
  case 'e':
  case 'o':
  case 'u':System.out.println("Vowel"); break;
  default:System.out.println("Constant");
 }
}
}
